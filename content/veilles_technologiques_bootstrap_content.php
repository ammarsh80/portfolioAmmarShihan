            <p>- des modèles de grille réactifs (=> Responsif :)), </p>
            <p>- des composants prêts à l'emploi (=> copier-coller :)), </p>
            <p>- des styles CSS personnalisés,</p>
            <p>- et des interfaces en ligne pour faciliter le développement web. </p><br>
