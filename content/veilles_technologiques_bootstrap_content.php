            <p>Et JavaScript, Bootstrap gère-t-il JavaScript?</p>
            <p>Avant, il fallait installer jQuery pour que ça fonctionne …
                Maintenant, Bootstrap 5 a ses propres plugins !
            </p>
            <p>En effet,
                Bien que le CSS Bootstrap puisse être utilisé avec n'importe quel framework css (Materialize, Skeleton, Semantic UI….. etc.),
                le JavaScript Bootstrap n'est pas entièrement compatible avec les frameworks JavaScript (tels que React, Vue et Angular) qui supposent une connaissance complète du DOM.
                Du coup, tous les deux (Bootstrap et un de ces frameworks) peuvent contrôler le même élément DOM, ce qui entraîne des bugs.
                Bootstrap propose comme solution d'utiliser un package spécifique au framework au lieu du JavaScript Bootstrap. les plus populaires :
            </p>
            <p>- Pour React: React Bootstrap</p>
            <p>- Pour Vue: BootstrapVue</p>
            <p>- Pour Angular: ng-bootstrap</p>
